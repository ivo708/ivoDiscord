package com.ivodiscord;

public class UserData {
    private String mcuser;
    private String mcuuid;
    private String discuser;
    private String discid;

    // Getters y Setters
    public String getMcuser() {
        return mcuser;
    }
    public void setMcuser(String mcuser) {
        this.mcuser = mcuser;
    }
    
    public String getMcuuid() {
        return mcuuid;
    }
    public void setMcuuid(String mcuuid) {
        this.mcuuid = mcuuid;
    }
    
    public String getDiscuser() {
        return discuser;
    }
    public void setDiscuser(String discuser) {
        this.discuser = discuser;
    }
    
    public String getDiscId() {
        return discid;
    }
    public void setDiscId(String discid) {
        this.discid = discid;
    }
}
